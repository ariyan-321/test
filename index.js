let mark=prompt("Enter the mark");
if (mark >=90 && mark<=100){
  alert("You got AA")
}else if (mark >=80 && mark<=89){
  alert("You got A+")
}else if (mark >=70 && mark<=79){
  alert("You got A")
}else if (mark >=60 && mark<=69){
  alert("You got A-")
}else if (mark >=50 && mark<=59){
  alert("You got B")
}else if (mark >=40 && mark<=49){
  alert("You got D")
}else if (mark > 0 && mark<33){
  alert("You got F")
}else{
  alert("Invalid number")
}

